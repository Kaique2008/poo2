package poo_02;

public class aviao {

    String marca;
    String modelo;
    int preco;
    String lugares;
    
    public String getMarca (String string) {
    	return marca;
    }
    public void setMarca(String marca) {
    	this.marca = marca;
    }
    public String getModelo (String string) {
    	return modelo;
    }
    public void setModelo(String modelo) {
    	this.modelo = modelo;
    }
    public int getPreco (int i) {
    	return preco;
    }
    public void setPreco(int preco) {
    	this.preco = preco;
    }
    public String getLugares (int i) {
    	return lugares;
    }
    public void setLugares(String lugares) {
    	this.lugares = lugares;
    }
}
